import UIKit

extension UIColor {
    
    class func clapperMainColor() -> UIColor {
        return UIColor(red: 0, green: 150/255, blue: 136/255, alpha: 1)
    }
    
    class func clapperLightColor() -> UIColor {
        return UIColor(red: 236/255, green: 240/255, blue: 241/255, alpha: 1)
    }
    
    class func clapperDarkColor() -> UIColor {
        return UIColor(red: 68/255, green: 68/255, blue: 68/255, alpha: 1)
    }
}
